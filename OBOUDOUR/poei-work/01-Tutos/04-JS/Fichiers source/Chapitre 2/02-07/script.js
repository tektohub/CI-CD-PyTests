// Donc le resultat ici est le nombre: 13
Math.round(12.9);

// Le resultat ici est le nombre: 12
Math.round(12.4);

// Retourne le calcul de 2 à la puissance 8 (2 correspond à la base et 8 à l'exposant).
// Donc le resultat ici est le nombre: 256
Math.pow(2, 8);

//Retourne un nombre pseudo-aléatoire compris entre 0 (inclus) et 1 (exclu).
Math.random();

// Retourne la plus grande valeur d'une liste de nombres.
// Donc le resultat ici est le nombre: 5
Math.max(2, 3, 4, 5);

// Retourne la plus petite valeur d'une liste de nombres.
// Donc le resultat ici est le nombre: 2
Math.min(2, 3, 4, 5);

//Quotient de la circonférence d'un cercle par son diamètre, environ 3,14159
Math.PI;

// Retourne le plus petit entier supérieur ou égal à la valeur passée entre parenthèses.
// Donc le resultat ici est le nombre: 13
Math.ceil(12.22);

// Retourne la racine carrée d'un nombre.
// Donc le resultat ici est le nombre: 2
