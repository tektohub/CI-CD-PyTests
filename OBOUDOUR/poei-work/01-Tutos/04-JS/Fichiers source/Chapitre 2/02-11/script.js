++score;
console.log(score); // Opérateur unaire. Ajoute un à son opérande

// Si l'opérateurs d'incrémentation est utilisé en suffixe (score++), 
// score est incrémenté après que le calcule se fait.
var resultat = score++ * 2;
console.log(score);
console.log("Le résultat est: " + resultat);

