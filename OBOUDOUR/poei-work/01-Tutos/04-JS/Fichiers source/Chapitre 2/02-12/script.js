var a = 10, b = 12;

// Cet opérateur permet d'ajouter la valeur de l'opérande droit à une variable,
// le résultat de l'addition étant affecté à cette variable. 
// Les types des deux opérandes déterminent le comportement de l'opérateur. 
// Selon le type, on pourra en effet avoir une addition ou une concaténation. 
a += b;
console.log(a);

// Cet opérateur soustrait la valeur de l'opérande droit à la variable 
// puis affecte le résultat de cette soustraction à la variable.
a -= b;

// Cet opérateur permet de multiplier une variable par la valeur de l'opérande droit 
// et d'affecter le résultat de cette opération à la variable.
a *= b;

// Cet opérateur permet de diviser une variable par la valeur de l'opérande droit 
// et d'affecter le résultat de cette opération à la variable.
a /= b;

